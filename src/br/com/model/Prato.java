package br.com.model;

public class Prato {

	private int codPrato;
	private String nomePrato;
	private String precoPrato;
	private String descricaoPrato;
	private float caloria;
	private float valorEnergetico;
	private float carboidrato;
	private float proteina;
	private float sodio;
	private float gorduras;
	private String imagemPrato;
	private int visitas;

	public int getCodPrato() {
		return codPrato;
	}
	public void setCodPrato(int codPrato) {
		this.codPrato = codPrato;
	}
	public String getNomePrato() {
		return nomePrato;
	}
	public void setNomePrato(String nomePrato) {
		this.nomePrato = nomePrato;
	}
	public String getPrecoPrato() {
		return precoPrato;
	}
	public void setPrecoPrato(String precoPrato) {
		this.precoPrato = precoPrato;
	}
	public String getDescricaoPrato() {
		return descricaoPrato;
	}
	public void setDescricaoPrato(String descricaoPrato) {
		this.descricaoPrato = descricaoPrato;
	}
	public float getCaloria() {
		return caloria;
	}
	public void setCaloria(float caloria) {
		this.caloria = caloria;
	}
	public float getValorEnergetico() {
		return valorEnergetico;
	}
	public void setValorEnergetico(float valorEnergetico) {
		this.valorEnergetico = valorEnergetico;
	}
	public float getCarboidrato() {
		return carboidrato;
	}
	public void setCarboidrato(float carboidrato) {
		this.carboidrato = carboidrato;
	}
	public float getProteina() {
		return proteina;
	}
	public void setProteina(float proteina) {
		this.proteina = proteina;
	}
	public float getSodio() {
		return sodio;
	}
	public void setSodio(float sodio) {
		this.sodio = sodio;
	}
	public float getGorduras() {
		return gorduras;
	}
	public void setGorduras(float gorduras) {
		this.gorduras = gorduras;
	}
	public String getImagemPrato() {
		return imagemPrato;
	}
	public void setImagemPrato(String imagemPrato) {
		this.imagemPrato = imagemPrato;
	}
	public int getVisitas() {
		return visitas;
	}
	public void setVisitas(int visitas) {
		this.visitas = visitas;
	}



}
