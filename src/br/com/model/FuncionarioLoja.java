package br.com.model;

public class FuncionarioLoja {

	private int codFuncionarioLoja;	
	private String nomeFuncionarioLoja;		
	private String cpfFuncionarioLoja;

	public String getNomeFuncionarioLoja() {
		return nomeFuncionarioLoja;
	}
	public void setNomeFuncionarioLoja(String nomeFuncionarioLoja) {
		this.nomeFuncionarioLoja = nomeFuncionarioLoja;
	}
	public String getCpfFuncionarioLoja() {
		return cpfFuncionarioLoja;
	}
	public void setCpfFuncionarioLoja(String cpfFuncionarioLoja) {
		this.cpfFuncionarioLoja = cpfFuncionarioLoja;
	}
	public int getCodFuncionarioLoja() {
		return codFuncionarioLoja;
	}
	public void setCodFuncionarioLoja(int codFuncionarioLoja) {
		this.codFuncionarioLoja = codFuncionarioLoja;
	}

}
